const mongoogse = require("mongoose");

const PostSchema = mongoogse.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoogse.model("Posts", PostSchema);
